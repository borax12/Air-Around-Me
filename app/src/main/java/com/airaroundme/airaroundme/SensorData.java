package com.airaroundme.airaroundme;

public class SensorData {

	private float ozone;
	private float ppm25;
	private float ppm10;
	private float co;
	private float so2;
	public float getOzone() {
		return ozone;
	}
	public void setOzone(float ozone) {
		this.ozone = ozone;
	}
	public float getPpm25() {
		return ppm25;
	}
	public void setPpm25(float ppm25) {
		this.ppm25 = ppm25;
	}
	public float getPpm10() {
		return ppm10;
	}
	public void setPpm10(float ppm10) {
		this.ppm10 = ppm10;
	}
	public float getCo() {
		return co;
	}
	public void setCo(float co) {
		this.co = co;
	}
	public float getSo2() {
		return so2;
	}
	public void setSo2(float so2) {
		this.so2 = so2;
	}
	
	
}
